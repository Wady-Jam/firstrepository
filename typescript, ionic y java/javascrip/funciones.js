function numeroMayor(valor1 , valor2){
    if (valor1 > valor2){ 
        return valor1;
    }
    return valor2;
} 

function numeroMenor(valor1, valor2){
    if (valor1 < valor2){
       return valor1;
    }
    return valor2;
}